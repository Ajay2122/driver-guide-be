# CORS / CSRF Configuration
# Helper function to normalize origins
def _normalize_origin(origin: str) -> str:
    origin = origin.strip()
    if not origin:
        return ''
    if origin.startswith('http://') or origin.startswith('https://'):
        return origin
    return f"https://{origin}"

# Default CORS origins for development
_default_cors = [
    "https://driver-guide-fe.vercel.app",
    "http://localhost:3000",
    "http://127.0.0.1:3000"
]

# Allow overrides via environment variable `CORS_ALLOWED_ORIGINS` (comma-separated)
env_cors = os.environ.get('CORS_ALLOWED_ORIGINS')
if env_cors:
    CORS_ALLOWED_ORIGINS = [
        _normalize_origin(o) for o in env_cors.split(',') if o.strip()
    ]
else:
    CORS_ALLOWED_ORIGINS = _default_cors

# CSRF trusted origins â€” can be the same as CORS origins or overridden with CSRF_TRUSTED_ORIGINS env
